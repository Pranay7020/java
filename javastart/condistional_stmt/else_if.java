package condistional_stmt;

public class else_if {  
public static void main(String args[]){
        int a=10;
        int b=5;
        if(a>b){
            System.out.println("a is greater than b");
        } else if(a==b) {
            System.out.println("a is equal to b");
        } else {
            System.out.println("a is less than b");
        }

    }
}